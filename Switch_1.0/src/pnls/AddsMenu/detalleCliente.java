    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        int msj = JOptionPane.showConfirmDialog(null,"¬øEstas seguro de querer editar este cliente?");
        if(JOptionPane.YES_OPTION == msj){
            try{
                Conexion con = new Conexion("jdbc:mysql://localhost:3306/e-wod","root","");
                Cliente_data cd = new Cliente_data(con);
                Cliente cliente = new Cliente(jlNombre.getText(), Long.parseLong(jtTelefono.getText()), jtEmail.getText(), jtBox.getText(), jtMetodo_de_pago.getText(), jtaComentario.getText());

                cd.actualizarCliente_por_id(cliente, Clientes1.cliente_pasado.getId());

                JOptionPane.showMessageDialog(null, "Se actualizo con exito el Cliente" );

                this.setVisible(false);
                Clientes1.addMenu.add(Clientes1.jlBackground);

            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "No se pudo cargar la tabla " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jbEditarActionPerformed

    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        int msj = JOptionPane.showConfirmDialog(null,"¬øEstas seguro de querer borrar este cliente?");
        if(JOptionPane.YES_OPTION == msj){
            try{
                Conexion con = new Conexion("jdbc:mysql://localhost:3306/e-wod","root","");
                Cliente_data cd = new Cliente_data(con);
                cd.borrarCliente(Clientes1.cliente_pasado);

                JOptionPane.showMessageDialog(null, "Se borro con exito el Cliente" );

                this.setVisible(false);
                Clientes1.addMenu.add(Clientes1.jlBackground);

            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "No se pudo cargar la tabla ");
            }
        }
    }//GEN-LAST:event_jbBorrarActionPerformed

